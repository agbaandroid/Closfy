package com.agba.closfy.modelo;

public class Asesoramiento {

	int idAsesoramiento;
	String idOrder;
	int consumido;

	public int getIdAsesoramiento() {
		return idAsesoramiento;
	}

	public void setIdAsesoramiento(int idAsesoramiento) {
		this.idAsesoramiento = idAsesoramiento;
	}

	public String getIdOrder() {
		return idOrder;
	}

	public void setIdOrder(String idOrder) {
		this.idOrder = idOrder;
	}

	public int getConsumido() {
		return consumido;
	}

	public void setConsumido(int consumido) {
		this.consumido = consumido;
	}

}
